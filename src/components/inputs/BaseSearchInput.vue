<template>
  <div
    ref="search"
    class="ui fluid search main-search-input"
    :class="{
      inverted: isDarkMode
    }"
  >
    <div class="ui icon fluid input">
      <input
        ref="input"
        class="prompt"
        type="text"
        :placeholder="searchText"
      >

      <BaseIcon
        icon="search"
      />
    </div>
  </div>
</template>

<script>
import {
  mapState
} from 'vuex'
import BaseIcon from '@/components/icons/BaseIcon.vue'
import {
  setSearch
} from '@/helpers/actions/plugins/semantic'
import {
  mainSearchOptions
} from '@/helpers/data/plugins/semantic'

export default {
  name: 'BaseSearchInput',
  components: {
    BaseIcon
  },
  props: {
    url: {
      type: String,
      required: true
    },
    fields: {
      type: Object,
      required: true
    },
    formatResponse: {
      type: Function,
      required: true
    }
  },
  emits: [
    'select'
  ],
  computed: {
    ...mapState(
      'layout',
      [
        'isDarkMode'
      ]
    ),
    ...mapState(
      'profile',
      {
        profileToken: 'token'
      }
    ),
    searchText () {
      return this.$t(
        'inputs.search'
      )
    },
    searchOptions () {
      return mainSearchOptions(
        {
          onResponse: this.formatResponse,
          url: this.urlFormatted,
          fields: this.fields,
          onSelect: this.handleSelect
        }
      )
    },
    urlFormatted () {
      return `${this.url}&token=${this.token}`
    },
    token () {
      return (
        this.profileToken ||
          this.anonymousToken
      )
    },
    anonymousToken () {
      return process.env.VUE_APP_ANONYMOUS_TOKEN
    }
  },
  watch: {
    url: 'handleUrlChange'
  },
  mounted () {
    this.initialize()
  },
  methods: {
    handleUrlChange () {
      this.initialize()
    },
    handleSelect (
      value
    ) {
      this.$emit(
        'select',
        value
      )
    },
    initialize () {
      setSearch(
        this.$refs.search,
        this.searchOptions
      )
    },
    focus () {
      this.$refs
        .input
        .focus()
    },
    clear () {
      this.$refs
        .input
        .value = ''
    }
  }
}
</script>

